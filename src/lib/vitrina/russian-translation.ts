const RULES = [
  ...PHRASES,
  ...Object.entries(WORDS),
].sort(([left], [right]) => right.length - left.length);

function escaped(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

export function russianProductType(name: string) {
  return PRODUCT_TYPES[name] ?? name;
}

export function translateCatalogText(value: string) {
  const capitalized = /^\p{Lu}/u.test(value);
  let translated = value;

  for (const [romanian, russian] of RULES) {
    translated = translated.replace(
      new RegExp(
        `(?<!\\p{L})${escaped(romanian)}(?!\\p{L})`,
        "giu",
      ),
      russian,
    );
  }

  translated = translated
    .replace(/(\d+)uși\b/giu, "$1 дверей")
    .replace(/(\d)(дверей|колеса|колесо|шт\.|см|мм)/gu, "$1 $2")
    .replace(/(\d)\s*mm\b/giu, "$1 мм")
    .replace(/(\d)\s*cm\b/giu, "$1 см")
    .replace(/\s{2,}/g, " ")
    .trim();

  return capitalized
    ? translated.charAt(0).toLocaleUpperCase("ru") + translated.slice(1)
    : translated;
}
